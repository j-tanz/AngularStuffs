import { Injectable } from '@angular/core';
import * as uuid from 'uuid';


export class Task {
  constructor(title: string, completed: boolean, detailText?: string) {
    this.title = title;
    this.completed = completed;
    this.detailText = detailText;

    this.taskId = uuid.v4(); //normally this would be generated by the server, but we'll generate it for example now
  }
  taskId: string;
  title: string;
  completed: boolean;

  detailText?: string;
}
@Injectable({
  providedIn: 'root'
})
export class TasksService {

  tasks: Task[] = [
    new Task('Walk the dog', false),
    new Task('Buy dog food', false)
  ]

  constructor() {
    this.tasks.push(new Task('Get some coding done!', true));
  }

  addTask(title: string, completed: boolean, detailText?: string) {
    const newTask = new Task(title, completed, detailText);
    this.tasks.push(newTask);
    console.log('New task added', newTask)
  }

  deleteTask(taskId: string) {
    this.tasks = this.tasks.filter((task) => {
      return task.taskId !== taskId
    })
  }

  getTasks(): Task[] {
    return this.tasks;
  }

}
